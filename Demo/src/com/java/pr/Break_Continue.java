package com.java.pr;

public class Break_Continue {
	public static void main(String args[]) {
	for (int i = 0; i < 10; i++) {
		  if (i == 4) {  //break
		    break;
		  }
		  System.out.println(i);
	}
	//continue
	for (int i = 0; i < 10; i++) {
		  if (i == 4) {
		    continue;
		  }
		  System.out.println(i);
		}
		}
	}


